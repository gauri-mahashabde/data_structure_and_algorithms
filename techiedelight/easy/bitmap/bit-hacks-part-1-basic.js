// Problem 1. Check if an integer is even or odd

const checkOddOrEven = num => {
    if ((num & 1) != 0) {
        console.log(`${num} is even`);
    } else {
        console.log(`${num} is even`);
    }
}

checkOddOrEven(8);
checkOddOrEven(9);